import re

date_pattern = r'\b(0[1-9]|[12][0-9]|3[01])[-/](0[1-9]|1[0-2])[-/](19|20)\d{2}\b | \b(0[1-9]|1[0-2])[-/](0[1-9]|[12][0-9]|3[01])[-/](19|20)\d{2}\b | \b(19|20)\d{2}[-/](0[1-9]|1[0-2])[-/](0[1-9]|[12][0-9]|3[01])\b'

test_dates = [
    "31-12-2023",
    "31/12/2023",
    "12-31-2023",
    "2023-12-31",
    "15-06-2020",  # valid
    "15/06/2020",  # valid
    "06-15-2020",  # valid
    "2020-06-15",  # valid
    "31-02-2023",  # invalid
    "2023/15/31",  # invalid
]

for date in test_dates:
    if re.search(date_pattern, date):
        print(f"{date}: Valid")
    else:
        print(f"{date}: Invalid")
